
#----------------------------------------------------------------------------
# ● 重置游戏数据
#----------------------------------------------------------------------------

module Taroxd end

module Taroxd::ClearData
  #------------------------------------------------------------------------
  # ● 导入
  #------------------------------------------------------------------------
  Game_Switches.send     :include, self
  Game_Variables.send    :include, self
  Game_SelfSwitches.send :include, self
  #------------------------------------------------------------------------
  # ● 重置
  #------------------------------------------------------------------------
  def clear
    @data.clear
    on_change
    self
  end
  alias reset clear
end