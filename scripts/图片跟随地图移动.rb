
#--------------------------------------------------------------------------
# ● require Taroxd基础设置
#    编号大于 50 的图片会跟随地图移动。
#--------------------------------------------------------------------------

Taroxd::PictureWithMap = true

class Game_Picture
  #--------------------------------------------------------------------------
  # ● 图片是否随地图移动
  #--------------------------------------------------------------------------
  def move_with_map?
    @number > 50
  end
  #--------------------------------------------------------------------------
  # ● X 坐标
  #--------------------------------------------------------------------------
  def_with :x do |old|
    old - (move_with_map? ? $game_map.display_x * 32 : 0)
  end
  #--------------------------------------------------------------------------
  # ● Y 坐标
  #--------------------------------------------------------------------------
  def_with :y do |old|
    old - (move_with_map? ? $game_map.display_y * 32 : 0)
  end
end

